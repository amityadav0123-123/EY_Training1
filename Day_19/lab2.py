trace_spans = []

def run_tool(name, args):
    start_time = time.perf_counter()
    fn = DISPATCH.get(name)
    if not fn:
        end_time = time.perf_counter()
        trace_spans.append({'tool': name, 'args': args, 'ms': (end_time - start_time) * 1000, 'ok': False, 'error': f'unknown tool {name}'})
        return {'error': f'unknown tool {name}'}, True
    try:
        out = fn(**args)
        end_time = time.perf_counter()
        is_err = isinstance(out, dict) and 'error' in out
        trace_spans.append({'tool': name, 'args': args, 'ms': (end_time - start_time) * 1000, 'ok': not is_err, 'result': out})
        return out, is_err
    except Exception as e:
        end_time = time.perf_counter()
        trace_spans.append({'tool': name, 'args': args, 'ms': (end_time - start_time) * 1000, 'ok': False, 'error': repr(e)})
        return {'error': repr(e)}, True

def print_trace_table():
    global trace_spans
    if not trace_spans:
        print('No tool calls made in this turn.')
        return

    print('\n--- Tool Call Trace ---')
    print(f"| {'Tool':<20} | {'Args':<40} | {'MS':>8} | {'OK':<5} |")
    print(f"| {'-'*20} | {'-'*40} | {'-'*8} | {'-'*5} |")
    for span in trace_spans:
        tool_name = span['tool']
        args_str = str(span['args'])
        if len(args_str) > 40: args_str = args_str[:37] + '...'
        ms = f"{span['ms']:.2f}"
        ok = '✅' if span['ok'] else '❌'
        print(f"| {tool_name:<20} | {args_str:<40} | {ms:>8} | {ok:<5} |")
    print('-----------------------')
    trace_spans = [] # Clear traces after printing

