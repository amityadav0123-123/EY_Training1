"""## Step 4 — The worker (consumer group)

In production this is a separate process. Here we drain the stream on demand. Each message is
processed, the result stored, and the message **acked** with `XACK` so it isn't redelivered.
"""

import threading, time

def run_worker(max_msgs=10):
    processed = 0
    resp = r.xreadgroup(GROUP, 'worker-1', {STREAM: '>'}, count=max_msgs, block=0)
    for _stream, msgs in resp or []:
        for msg_id, fields in msgs:
            f = {k.decode(): v.decode() for k, v in fields.items()}
            job_id = f['job_id']
            to = f['to']
            retries = int(f.get('retries', 0))

            # Simulate email sending failure for addresses containing 'fail'
            if 'fail' in to:
                print(f"[Worker] Simulating failure for job {job_id} (to: {to})")
                if retries < 2: # Max 2 retries
                    print(f"[Worker] Retrying job {job_id}. Retries: {retries+1}")
                    # Re-enqueue with incremented retry count
                    r.xadd(STREAM, {'job_id': job_id, 'to': to, 'subject': f['subject'], 'body': f['body'], 'retries': retries + 1})
                    r.set(f"jobresult:{job_id}", f"retrying {retries+1}")
                else:
                    print(f"[Worker] Moving job {job_id} to DLQ (max retries reached)")
                    r.xadd(DLQ_STREAM, {'job_id': job_id, 'to': to, 'subject': f['subject'], 'body': f['body'], 'status': 'failed'})
                    r.set(f"jobresult:{job_id}", 'failed_dlq')
            else:
                # In a real scenario, this would call a real email provider
                print(f"[Worker] Sending email for job {job_id} to {to}")
                r.set(f"jobresult:{job_id}", 'sent')

            r.xack(STREAM, GROUP, msg_id)
            processed += 1
    return processed

def inspect_dlq():
    dlq_messages = []
    # Note: Using XREAD here with a simple ID to read all available messages up to now
    # For a consumer group, you'd typically use XREADGROUP once to claim pending messages,
    # and then subsequent XREADGROUP calls to read new messages.
    resp = r.xread({DLQ_STREAM: '0-0'}, count=100, block=0)
    for _stream, msgs in resp or []:
        for msg_id, fields in msgs:
            f = {k.decode(): v.decode() for k, v in fields.items()}
            dlq_messages.append({'msg_id': msg_id.decode(), **f})
    return dlq_messages

