        _CURRENT_CUMULATIVE_COST += score['cost_usd'] if 'cost_usd' in score else 0 # Accumulate cost for judge

        return {"lead_id": lead["lead_id"], "status": "ok", "trace_id": tid,
                "outreach": safe_text, "grounded": g.allowed, "score": score}


