"""## Extension Task 4: Cost ceiling

This section implements a cost ceiling, aborting the batch processing if the cumulative cost exceeds a predefined budget.
"""

COST_BUDGET = 0.0005 # A very tight budget for demonstration purposes
_CURRENT_CUMULATIVE_COST = 0.0

# Redefine run_lead to include a cost ceiling
def run_lead(lead: dict) -> dict:
    global _CURRENT_CUMULATIVE_COST

    # Check budget before processing the lead
    if _CURRENT_CUMULATIVE_COST >= COST_BUDGET:
        log_event("budget.exceeded", level="CRITICAL", reason="Cumulative cost exceeded budget",
                  current_cost=_CURRENT_CUMULATIVE_COST, budget=COST_BUDGET)
        return {"lead_id": lead["lead_id"], "status": "aborted", "reason": "budget_exceeded"}

    with span("pipeline.lead", lead_id=lead["lead_id"]) as root_otel_span:
        # Retrieve trace_id from the OpenTelemetry span
        current_span_context = root_otel_span.get_span_context()
        tid = f'{current_span_context.trace_id:x}'

        # --- input guardrails ---
        checks = check_input(lead)
        flags = [g.rule for g in checks if not g.allowed]
        if flags:
            audit("system", "input_guard", "-", str(lead), "", {}, flags, "block", tid)
            return {"lead_id": lead["lead_id"], "status": "blocked", "flags": flags}

        # --- research ---
        with span("agent.researcher"):
            enr = instrumented_call(f"One pain point for: {lead}", model=MODEL_ROUTINE)
            _CURRENT_CUMULATIVE_COST += enr.cost_usd # Accumulate cost
            audit("system", "researcher", MODEL_ROUTINE, str(lead), enr.text,
                  {"temperature": 0.2}, [], "allow", tid)

        # --- Check budget after research step ---
        if _CURRENT_CUMULATIVE_COST >= COST_BUDGET:
            log_event("budget.exceeded", level="CRITICAL", reason="Cumulative cost exceeded budget",
                      current_cost=_CURRENT_CUMULATIVE_COST, budget=COST_BUDGET, lead_id=lead["lead_id"], stage="research")
            return {"lead_id": lead["lead_id"], "status": "aborted", "reason": "budget_exceeded"}

        # --- summarise + grounding guard ---
        with span("agent.summariser"):
            summ = instrumented_call(f"Summarise for sales: {lead} pain: {enr.text}",
                                     model=MODEL_JUDGEMENT)
            _CURRENT_CUMULATIVE_COST += summ.cost_usd # Accumulate cost
            g = gr_grounded(summ.text, lead.get("notes", "") + enr.text)
            audit("system", "summariser", MODEL_JUDGEMENT, "summarise", summ.text,
                  {"temperature": 0.2}, [] if g.allowed else [g.rule], "allow", tid)

        # --- Check budget after summariser step ---
        if _CURRENT_CUMULATIVE_COST >= COST_BUDGET:
            log_event("budget.exceeded", level="CRITICAL", reason="Cumulative cost exceeded budget",
                      current_cost=_CURRENT_CUMULATIVE_COST, budget=COST_BUDGET, lead_id=lead["lead_id"], stage="summariser")
            return {"lead_id": lead["lead_id"], "status": "aborted", "reason": "budget_exceeded"}

        # --- notify + PII redaction before returning/logging ---
        with span("agent.notifier"):
            out = instrumented_call(f"One-line outreach for: {summ.text}", model=MODEL_ROUTINE)
            _CURRENT_CUMULATIVE_COST += out.cost_usd # Accumulate cost
