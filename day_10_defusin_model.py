import torch
import torchvision
import torchvision.transforms as transforms

# Define a linear noise schedule
T = 1000 # Total timesteps
betas = torch.linspace(0.0001, 0.02, T)

# Precompute alphas and other terms
alphas = 1.0 - betas
alpha_bars = torch.cumprod(alphas, dim=0)

# Function to add noise at any timestep t
def q_sample(x_0, t, alpha_bars):
    noise = torch.randn_like(x_0)
    sqrt_alpha_bar = torch.sqrt(alpha_bars[t])
    sqrt_one_minus_alpha_bar = torch.sqrt(1.0 - alpha_bars[t])
    return sqrt_alpha_bar * x_0 + sqrt_one_minus_alpha_bar * noise, noise

# Re-define dataset for this cell to ensure x0 can be accessed
transform = transforms.Compose([
    transforms.ToTensor(),
    transforms.Normalize((0.5,), (0.5,))
])

dataset = torchvision.datasets.CIFAR10(
    root="./data",
    train=True,
    download=True,
    transform=transform
)

# Select the original image (x0) from earlier cells
image_index = 0 # Ensure image_index is defined
x0, _ = dataset[image_index] # Re-initialize x0 within this cell

# Visualize the forward diffusion process at various timesteps
num_steps_to_show = 8
steps = torch.linspace(0, T - 1, num_steps_to_show).long()

plt.figure(figsize=(20, 3))
plt.suptitle(f"Forward Diffusion Process: {num_steps_to_show} Timesteps")

for i, t in enumerate(steps):
    xt, _ = q_sample(x0, t, alpha_bars)
    plt.subplot(1, num_steps_to_show, i + 1)
    plt.imshow(xt.permute(1, 2, 0).clamp(0, 1))
    plt.title(f"t={t.item()}")
    plt.axis("off")

plt.tight_layout(rect=[0, 0.03, 1, 0.95]) # Adjust layout to make space for suptitle
